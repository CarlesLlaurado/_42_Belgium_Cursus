#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*dst;
	int		i;
	int		len;

	len = 0;
	while (src[len] != 0)
		len++;
	dst = (char *)malloc(sizeof(char) * (len + 1));
	if (!dst)
		return (0);
	i = 0;
	while (i < len)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = 0;
	return (dst);
}
